    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       
    String placaBuscada = txtPlaca.getText().trim().toUpperCase();
    
    // Validar que se haya ingresado una placa
    if (placaBuscada.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
                "Por favor ingrese una placa para buscar", 
                "Campo vacÃ­o", 
                JOptionPane.WARNING_MESSAGE);
        txtPlaca.requestFocus();
        return;
    }
    
    // Limpiar la tabla antes de buscar
    modeloTabla.setRowCount(0);
    vehiculoEncontrado = null;
    
    // Buscar el vehÃ­culo en el ArrayList
    boolean encontrado = false;
    for (Vehiculo v : MenuOpciones.registrarVehiculo) {
        if (v.getPlaca().equalsIgnoreCase(placaBuscada)) {
            vehiculoEncontrado = v;
            encontrado = true;
            
            // Agregar el vehÃ­culo a la tabla
            Object[] fila = new Object[3];
            fila[0] = v.getPlaca();
            fila[1] = v.getMarca();
            fila[2] = v.getHoraEntrada().format(formatter);
            
            modeloTabla.addRow(fila);
            break;
        }
    }
    // Si no se encontrÃ³ el vehÃ­culo
    if (!encontrado) {
        JOptionPane.showMessageDialog(this, 
                "No se encontrÃ³ ningÃºn vehÃ­culo con la placa: " + placaBuscada + "\n" +
                "Verifique que el vehÃ­culo estÃ© registrado en el parqueo.", 
                "VehÃ­culo no encontrado", 
                JOptionPane.ERROR_MESSAGE);
    } else {
        // Mostrar mensaje de Ã©xito
        JOptionPane.showMessageDialog(this, 
                "VehÃ­culo encontrado", 
                "Ãxito", 
                JOptionPane.INFORMATION_MESSAGE);
    }//fin de la    
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

